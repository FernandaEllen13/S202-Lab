#criando as classes:

alunos = []

class Professor:
    def __init__(self, nome):
        self.nome = nome

    def ministrar_aula(self):
        print(f'O professor {self.nome} está ministrando uma aula.')        



class Aluno:
    def __init__(self, nome):
        self.nome = nome

    def presenca(self):
        print(f'"O aluno {self.nome} está presente')


class Aula:
    def __init__(self, professor, assunto):
        self.professor = Professor.nome
        self.assunto = assunto

    def adicionar_aluno(aluno):
        aluno = Aluno.nome
        alunos.append(aluno)

    def listar_presenca(self):
        print(f'Presença na aula sobre {self.assunto}, ministrada pelo professor {self.professor}:')
        print(alunos)



professor = Professor("Lucas")
aluno1 = Aluno("Maria")
aluno2 = Aluno("Pedro")
aula = Aula(professor, "Programação Orientada a Objetos")
aula.adicionar_aluno(aluno1)
aula.adicionar_aluno(aluno2)
print(aula.listar_presenca())






